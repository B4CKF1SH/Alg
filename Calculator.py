def Div(Num_1, Num_2):
    print(f"Division of {Num_1} and {Num_2} is {Num_1/Num_2}.")
    return Num_1/Num_2

def Expo(Num_1, Num_2):
    print(f"{Num_1} to the power {Num_2} is {Num_1**Num_2}.")
    return Num_1**Num_2

def Int_Div(Num_1, Num_2):
    print(f"Integral division of {Num_1} and {Num_2} is {Num_1//Num_2}.")
    return Num_1//Num_2

def Multi(Num_1, Num_2):
    print(f"Multiplication of {Num_1} and {Num_2} is {Num_1*Num_2}.")
    return Num_1*Num_2

def Remain(Num_1, Num_2):
    print(f"Modules of {Num_1} and {Num_2} is {Num_1%Num_2}.")
    return Num_1 % Num_2

def Sub(Num_1, Num_2):
    print(f"Difference of {Num_1} and {Num_2} is {Num_1-Num_2}.")
    return Num_1-Num_2

def Sum(Num_1, Num_2):
    print(f"Sum of {Num_1} and {Num_2} is {Num_1+Num_2}.")
    return Num_1+Num_2
